package org.ctrlstmntcpt;

public class IfLadder {

	public static void main(String[] args) {
		int a=10;
		
		
		if(a>10) {
			System.out.println("cond1");
		}
		else if(a<5) {
			System.out.println("cond2");
		}
		else if(a==10) {
			System.out.println("cond3");
		}
		else {
			System.out.println("invalid");
		}

	}

}
